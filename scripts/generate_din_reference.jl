function spl28(file, dir)
    path = joinpath(dir, file)
    Downloads.download("$BASE/$file", path)
    raw, fs, _, _ = wavread(path; format="native")
    eltype(raw) <: Int16 || error("$file: expected Int16 samples, got $(eltype(raw))")
    sig = Float64.(raw[:, 1]) ./ (2^15 - 1)
    spl = band_levels(sig, Float64(fs))
    overall = 10 * log10(sum(10 .^ (spl ./ 10)))
    @info file overall_dB = round(overall; digits=1)
    return spl
end

function emit(io, name, rows, dir)
    println(io, "const $name = [")
    for (fc, file, S) in rows
        vals = join((@sprintf("%.6f", v) for v in spl28(file, dir)), ", ")
        println(io, "    ($fc, $S, [$vals]),")
    end
    println(io, "]")
end

