# Downloads the DIN 45692:2009 chapter-6 reference test signals as vendored
# by MoSQITo (Apache-2.0, https://github.com/Eomys/MoSQITo) and reduces each
# to a 28-band one-third-octave SPL vector via ZwickerLoudnessAudio.
#
# Calibration follows MoSQITo mosqito/utils/load.py: int16 sample / (2^15 - 1)
# is the signal in pascals (1 Pa/FS), first channel only. The signals'
# overall levels are ~65-69 dB (narrowband) / ~50 dB (broadband) — they are
# NOT 60 dB; do not "fix" this.
#
# Reference sharpness values are transcribed from MoSQITo
# validations/sq_metrics/sharpness_din/validation_sharpness_din.py, which
# cites DIN 45692_2009E chapter 6.
#
# Usage: julia scripts/generate_din_reference.jl   (needs network access)

